package com.vbagetech.workingwithvolley.VolleyAPIPack;

public class ParamsData {
    String KEY,VALUE;

    public ParamsData(String key,String value){
        this.KEY=key;
        this.VALUE=value;
    }
    public String getKEY() {
        return KEY;
    }

    public String getVALUE() {
        return VALUE;
    }
}
